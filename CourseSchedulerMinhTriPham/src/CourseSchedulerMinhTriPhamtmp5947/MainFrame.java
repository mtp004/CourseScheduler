    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        String courseCode=selectClassComboBox.getSelectedItem().toString();
        StudentEntry student=(StudentEntry)selectStudentComboBox.getSelectedItem();
        java.sql.Timestamp currentTimestamp=new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        try{
            if(courseCode.equals("") || student==null){
                throw new IllegalArgumentException();
            }else if((int)ClassQueries.getClassSeats(currentSemester,courseCode)>0){
                String studentID=student.GetStudentID();
                ScheduleEntry schedule=new ScheduleEntry(currentSemester, courseCode,studentID,"s",currentTimestamp);
                ScheduleQueries.addScheduleEntry(schedule);
                scheduleClassStatusLabel.setText(courseCode+" has been scheduled for "+student);
            } else{
                String studentID=student.GetStudentID();
                ScheduleEntry schedule=new ScheduleEntry(currentSemester, courseCode,studentID,"w",currentTimestamp);
                ScheduleQueries.addScheduleEntry(schedule);
                scheduleClassStatusLabel.setText(courseCode+" has been waitlisted for "+student);
            }
        } catch(IllegalArgumentException exception){
            scheduleClassStatusLabel.setText("ERROR: All fields must have a value!");
        }
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

