    private void displayScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleButtonActionPerformed
        StudentEntry student=(StudentEntry)displayScheduleComboBox.getSelectedItem();
        ArrayList<ScheduleEntry> schedule=ScheduleQueries.getScheduleByStudent(currentSemester,student.GetStudentID());
        DefaultTableModel displayScheduleTableModel=(DefaultTableModel) displayScheduleTable.getModel();
        
        displayScheduleTextArea.setText("Course code\tStatus\n");
        
        for(ScheduleEntry scheduleEntry: schedule){
            if(scheduleEntry.GetStatus().equals("s")){
                displayScheduleTextArea.append(scheduleEntry.GetCourseCode()+"\tScheduled\n");
            } else{
                displayScheduleTextArea.append(scheduleEntry.GetCourseCode()+"\tWaitlisted\n");
            }
        }
        
        displayScheduleTableModel.setNumRows(0);
        Object[] rowData=new Object[2];
        
        for(ScheduleEntry scheduleEntry: schedule){
            rowData[0]=scheduleEntry.GetCourseCode();
            
            if(scheduleEntry.GetStatus().equals("s")){
               rowData[1]="Scheduled";
            } else{
                rowData[1]="Waitlisted";
            }
            
            displayScheduleTableModel.addRow(rowData); 
        }
    }//GEN-LAST:event_displayScheduleButtonActionPerformed

    private void DropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentButtonActionPerformed
        StudentEntry student=(StudentEntry)DropStudentComboBox.getSelectedItem();
        dropStudentTextArea.setText("");
        try{
            if(student==null){
                throw new IllegalArgumentException();
            } else{
                ArrayList<ScheduleEntry> scheduleEntries=ScheduleQueries.getScheduleByStudent(currentSemester, student.GetStudentID());
                for(ScheduleEntry scheduleEntry:scheduleEntries){
                    ArrayList<ScheduleEntry> waitlisted=ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, scheduleEntry.GetCourseCode());
                    
                    if (waitlisted.isEmpty()==false){
                        if (scheduleEntry.GetStatus().equals("s")){
                            StudentEntry waitlistedStudent=StudentQueries.getStudent(waitlisted.get(0).GetStudentID());
                            ScheduleQueries.updateScheduleEntry(waitlisted.get(0));
                            dropStudentTextArea.append("Wailisted "+waitlistedStudent.GetLastName()+", "+waitlistedStudent.GetFirstName()+" "+waitlistedStudent.GetStudentID()+" is now enrolled in "+scheduleEntry.GetCourseCode()+"\n");
                        }
                    } else{
                        if (scheduleEntry.GetStatus().equals("s")){
                            ClassQueries.updateSeats(currentSemester, scheduleEntry.GetCourseCode(), 1);
                        }
                    }
                    ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.GetStudentID(), scheduleEntry.GetCourseCode());
                dropStudentTextArea.append(student.GetLastName()+", "+student.GetFirstName()+" "+student.GetStudentID()+" has been dropped from "+scheduleEntry.GetCourseCode()+"\n");
                dropStudentTextArea.append("\n");
                }
            }
        } catch(IllegalArgumentException exception){
            dropStudentTextArea.setText("ERROR: student must have a value!");
        }
    }//GEN-LAST:event_DropStudentButtonActionPerformed

